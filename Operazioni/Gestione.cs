using System;

namespace Operazioni
{
    public class Gestione
    {
        public static int SommaInt(int a, int b)
        {
            return a + b;
        }
        public static double SommaDecimale(double a, double b)
        {
            return a + b;
        }
        public static int DifferenzaInt(int a, int b)
        {
            return a - b;
        }
        public static double DifferenzaDecimale(double a, double b)
        {
            return a - b;
        }
        public static int MoltiplicazioneInt(int a, int b)
        {
            return a * b;
        }
        public static double MoltiplicazioneDecimale(double a, double b)
        {
            return a * b;
        }
        public static int DivisioneInt(int a, int b)
        {
            return a / b;
        }
        public static double DivisioneDecimale(double a, double b)
        {
            return a / b;
        }
    }
}
